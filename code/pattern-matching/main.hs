#!/usr/bin/env runghc
-- coding: utf-8


-- ============================================================================
-- 階乗
-- ============================================================================
fact1 :: Integer -> Integer
fact1 0 = 1
fact1 n =
	if n == 0
	then 1
	else n * fact1 (n - 1)

-- ============================================================================
-- fact1 をパターンマッチで書き直したもの
-- ============================================================================
fact2 :: Integer -> Integer
fact2 0 = 1
fact2 n = n * fact2 (n - 1)

-- ============================================================================
-- エントリーポイント
-- ============================================================================
main :: IO ()
main = do
	print $ fact1 0
	print $ fact1 1
	print $ fact1 2
	print $ fact1 3
	print $ fact1 4
	print $ fact1 5
